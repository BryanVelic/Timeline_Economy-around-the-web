/*------------------------------Modal 1969------------------------*/
var modal1969 = document.getElementById("modal_1969");
var btn1969 = document.getElementById("btn_modal_1969");
var span1969 = document.getElementsByClassName("close1969")[0];
btn1969.onclick = function() {
  modal1969.style.display = "block";
}
span1969.onclick = function() {
  modal1969.style.display = "none";
}
window.onclick = function(event) {
  if (event.target == modal1969) {
    modal1969.style.display = "none";
  }
}

